package lk.ijse;

public class Pattern {
    public static void main(String[] args) {

        int [][] nums = new int[10][10];


        for (int i = 0; i < nums.length; i++) {
            for (int j = 0; j < nums.length; j++) {
                System.out.println(nums[j] [i] + " ");
            }
            System.out.println();
        }

    }
}
